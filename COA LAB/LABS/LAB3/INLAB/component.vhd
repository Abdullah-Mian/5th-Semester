library ieee; 

use ieee.std_logic_1164.all;

package components is 
	component HalfAdder is 
		port (
        a : in  STD_LOGIC;
        b : out STD_LOGIC;
		  s : out STD_LOGIC;
		  c : out STD_LOGIC
    );
end component;

component FullAdder is
    port (
        a : in  STD_LOGIC;
        b : out STD_LOGIC;
		  c : out STD_LOGIC;
		  sum : out STD_LOGIC;
		  carry : out STD_LOGIC
    );
end component;

end components;